        <h3>Sejarah</h3>
        <p class="text-justify">Desa Topidi adalah desa yang terletak di Kelurahan Bontolerung, Kecamatan Tinggimoncong, Kabupaten Gowa, Provinsi Sulawesi Selatan merupakan desa yang memiliki sumber daya alam yang melimpah. Desa ini ikonik dengan kopi nya yang unik, yaitu kopi topidi. Selain sumber daya alam, desa ini juga memiliki destinasi wisata seperti Air Terjun Kuda Sembrani dan Langit Topidi Resort.</p>
        <p class="text-justify">Topidi, sesuai dengan letaknya yang tersembunyi dalam akses dan catatan sejarah atas keberadaannya di Sulsel. Hampir tidak ditemukan penulisan tentang Topidi. Namanya pun terlindung di balik kepopuleran Malino. Topidi, mungkin hanya terdengar dari komunitas touring, kelompok pencinta alam, atau kegiatan pengkaderan mahasiswa. Lima tahun lalu desa Topidi masih ketegori dusun yang masuk dalam area desa Biroro kelurahan Bontolerung Kecamatan Tinggi Moncong, Kabupaten Gowa. Topidi sebagai dusun ditulis oleh Irma, salah seorang mahasiswa yang pernah melakukan kegiatan pengkaderan melalui sosiologi pedesaan.</p>
        <p class="text-justify">Akses menuju Topidi, dari dulu hingga saat ini masih berupa jalan yang dipenuhi bebatuan. Jalan  yang  berkelok dan menanjak menjadi tantangan tersendiri menuju daerah ini. Meski dapat dilalui kendaraan roda empat, namun harus ada yang mengalah untuk menepi dan berhenti. Jalan yang sempit dan penuh jurang. Jalan ini terwujud berkat gotong royong masyarakat Topidi membangun jalan sedikit demi sedikit. Secara swadaya mereka membangun jalan dengan batu kurang lebih 3 kilometer. Desa Topidi terletak di daerah yang dikeliling gunung dan perbukitan yang dihuni 38 KK. Kabar terakhir saat mendatangi Topidi pada 21 Juni 2020 lalu telah mencapai 40 KK. Tahun 1915, Topidi masih didiami 30 KK. Bentang alam Topidi yang berada di ketinggian, menjadi lahan subur tumbuhnya tanaman kopi. Letaknya yang berada tepat di kaki Gunung  Bawakaraeng, menyebabkan kontur tanah cenderung miring.</p>
        <p class="text-justify">Di Topidi, kabut dan dingin tak pernah punya akhir. Setiap yang datang membawa kisahnya tersendiri. Bagaimana menyaksikan pesona kabut dan bagaimana berdamai dengan dingin. Panorama bentangan alam dipersaksikan bak lukisan, mata seakan-akan mengandung spirit cahaya. Perjalanan kali ini membawa hal-hal baru yang membuat ingatan lama ikut juga hadir, walau tanpa diminta. Tapi selalu ada yang tak berubah pada kita sejak sebelum berangkat. Ini tentang kopi. Rasa penasaran akan Topidi membuat perjalanan kali ini bisa terwujud. Menurut Daeng Halim, salah seorang petani kopi dan juga warga topidi, kopi pertama kali ada jenis robusta. Tanaman kopi robusta telah ada di Topidi sejak zaman Belanda ungkap Daeng Halim. Keberadaan Kopi jenis Arabika mulai ditanam sejak 36 tahun yang lalu. Kopi tersebut ditengarai ditanam secara turun termurun dari warga Topidi. Jika kita melihat hamparan lahan, hampir semua tanamam kopi yang ada sekarang adalah kopi Arabika. Itulah yang dikenal saat ini sebagai kopi topidi. Kopi ini sendiri tumbuh di ketinggian kurang lebih 1250 – 1500 mdpl. Kopi topidi telah berkembang pesat dan berkonstribusi atas penanaman kopi di Dusun Cindakko, Desa Bonto Somba, Kecamatan Tompobulu, Maros. Sebanyak 22.000 bibit kopi yang berasal dari lokasi pembibitan kopi di Desa Topidi, Malino, Kab. Gowa, Sulawesi Selatan. Informasi ini disampaikan oleh Daeng Anggi, salah seorang petani, saat bincang santai di atas rumah mungil di atas bukit. Secara historis, penamaan suatu kopi dengan identitas tempat bisa bermakna macam-macam. Kopi topidi telah menjadi nama yang disepakati para petani kopi di Topidi.</p>
        <p class="text-justify">Selain menawarkan keindahan pegunungan, suasana pedesaannya, dan ciri khas kopinya, Topidi juga menyembunyikan surga kecil mereka di dalam pegunungan hutan tropisnya. Apa yang ada di dalam hutan itu adalah Air terjun setinggi 80 meter. Cukup dengan menempuh 30 menit berjalan kaki dari pemukiman warga, Anda sudah bisa menikmati keindahan Air terjun yang ada di tengah hutan dan dikelilingi tebing ini. Belum banyak orang yang mengunjungi Air terjun ini semenjak ditemukan oleh warga lokal 30 tahun lalu, hanya warga sekitar yang melewatinya kalau mau berkebun dan kenalan dekat dari luar kota yang diajak kesana. Air Terjun tersebut adalah Air Terjun Berkuda Sembrani. Air Terjun ini belum terekspos dengan baik sehingga masih banyak warga sekitar apalagi tamu-tamu dari luar desa Topidi yang belum tau betul letak air terjun tersebut. Padahal lokasinya hanya sekitar 1km dari pemukiman warga, namun karena akses jalannya yg agak licin dan harus melalui perkebunan kopi sehingga menjadi kendala yg essensial. Penamaan air terjun berkuda ini diberikan oleh tetuah kampung Toppidi dengan alasan bahwa ke air terjun tersebut hanya bisa di tempuh dengan menunggangi kuda. </p>
